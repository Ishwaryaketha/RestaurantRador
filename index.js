  function getrestaurants(restaurants){
    restaurants.forEach(restaurant => {
        const body=document.querySelector('body');
        //content
        const content_div=document.createElement('div');
        content_div.className="content";
        //content1
        const content1=document.createElement('div');
        content1.className="content1";
        const restaurant_name=document.createElement('span');
        restaurant_name.className="restaurant_name";
        restaurant_name.innerHTML=restaurant['name'];
        const rating=document.createElement('span');
        rating.className="rating";
        rating.innerHTML=restaurant['rating']+'⭐';
         
        content1.appendChild(restaurant_name);
        content1.appendChild(rating)
        //content2
        const content2=document.createElement('div');
        content2.className="content2";
        const price=document.createElement('span');
        price.className="price";
        price.innerHTML='₹'+restaurant['price_for_two'];

        const foodtype=document.createElement('span');
        foodtype.className="foodtype";
        foodtype.innerHTML=restaurant['foodtype'];

        content2.appendChild(foodtype);
        content2.appendChild(price);
        //content3
        const content3=document.createElement('div');
        content3.className="content3";
        const distance=document.createElement('span');
        distance.className="distance";
        distance.innerHTML=restaurant['distance']+'km';
        const address=document.createElement('span');
        address.className="address";
        address.innerHTML=restaurant['location'];

        content3.appendChild(address);
        content3.appendChild(distance);

        content_div.appendChild(content1);
        content_div.appendChild(content2);
        content_div.appendChild(content3);
        //card_div
        const card_div=document.createElement('div');
        card_div.className="card";

        card_div.onclick = () => {
          localStorage.setItem("selectedRestaurant", JSON.stringify(restaurant));
          window.location.href = "restaurantDetails.html";
        };
        //image
        const restaurant_image=document.createElement('div');
        restaurant_image.className="restaurant_image";
        const image=document.createElement('img');
        image.src=`images/${restaurant.image}.jpg`;
        image.style.height='250px';
        image.style.width='250px';
        restaurant_image.appendChild(image);

        card_div.appendChild(restaurant_image);
        card_div.appendChild(content_div);

        root.appendChild(card_div);
        body.appendChild(root);
    });
  }

