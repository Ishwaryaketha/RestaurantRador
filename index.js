  function displayRestaurants(filteredRestaurantNames) {
    const restaurantList = document.getElementById("restaurantList");
    restaurantList.innerHTML = ""; // Clear the existing list
  
    if (filteredRestaurantNames.length > 0) {
      restaurantList.style.display = "block"; // Show the list
      filteredRestaurantNames.forEach((restaurantName) => {
        const li = document.createElement("li");
        li.textContent = restaurantName;
  
        // Find the restaurant data by name
        const restaurantData = restaurants.find(
          (r) => r.name.toLowerCase() === restaurantName.toLowerCase()
        );
  
        // Store restaurant data in a custom attribute
        li.setAttribute("data-restaurant", JSON.stringify(restaurantData));
  
        // Add click event
        li.onclick = () => {
          if (restaurantData) {
            localStorage.setItem("selectedRestaurant", JSON.stringify(restaurantData));
            window.location.href = "./restaurantDetails.html";
          }
        };
  
        restaurantList.appendChild(li);
      });
    } else {
      restaurantList.style.display = "none"; // Hide the list if no matches
    }
  }
  

  
